import java.util.*;


public class EvenorOddnumber {

	public static void main(String[] args) {

		Scanner S= new Scanner(System.in);
		System.out.println("Enter the number");
		int x = S.nextInt();

		if (x%2==0)
		{
		System.out.println("number is even");
		}
			
		else
		{
			
			System.out.println("number is odd");
		}
			
	}

}
