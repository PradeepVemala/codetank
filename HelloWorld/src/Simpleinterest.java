
public class Simpleinterest {

	public static void main(String[] args) {

		double SI;
		int p= 10000;
		double t = 2.5;
		double r = 11.5;
		
	SI=(p*t*r/100);
	
System.out.println("The Simple interest on the principle taken would be "+SI);
		
double amount= SI+p;

System.out.println("The total amount I would receive is "+amount);
	}

}
